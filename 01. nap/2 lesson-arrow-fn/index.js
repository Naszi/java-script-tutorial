// több paraméter
const add = (a, b) => a + b;
console.log(add(5, 10));

// egy paraméter
const square = x => x ** 2;
console.log(square(5));

// paraméter nélkül
const greet = () => console.log('Hello');
greet();

const doubleNumber = (n) => n * 2;
console.log(doubleNumber(5));
console.log(doubleNumber(8));

const isEvenNumber = (n) => n % 2 === 0;
console.log(isEvenNumber(8));
console.log(isEvenNumber(7));

